public class AssignmentOperators{
	public static void main(String[] args){
			int a = 5;
			int b = 6;
			a+=b;
			System.out.println(":a:"+a);//11
			a-=b;
			System.out.println(":a:"+a);//5
			a*=b;
			System.out.println(":a:"+a);//30
			a/=b;
			System.out.println(":a:"+a);//5
			a%=b;
			System.out.println(":a:"+a);//0
	}

}